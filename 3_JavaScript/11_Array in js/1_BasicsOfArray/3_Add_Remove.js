//Add Element in Array-
//1. push() -> add element in the last of array
//2. unshift() -> add element in the starting of array

let arr=[2,3];
arr.push(6);
console.log(arr);

arr.unshift(5);
console.log(arr);

//Remove Element from Array-
//1. pop() -> Remove element from last of array
//2. shift() -> Remove element from staring of array
let arr1=[9,5,6,7];
arr1.pop();
console.log(arr1);

arr1.shift();
console.log(arr1);